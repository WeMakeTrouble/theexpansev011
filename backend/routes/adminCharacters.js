/* ============================================================================
 * POST /:id/inventory/transfer — Transfer object to another character
 * ============================================================================
 * Moves an object from source character to target character.
 * Logs the transfer in object_transfer_history.
 * Enforces single ownership (DB UNIQUE constraint on object_id).
 * ============================================================================ */

router.post('/:id/inventory/transfer', async (req, res, next) => {
  const startTime = performance.now();
  const client = await pool.connect();

  try {
    const fromCharacterId = decodeURIComponent(req.params.id);

    if (!isValidHexId(fromCharacterId)) {
      const error = new Error('Invalid source character ID format.');
      error.statusCode = 400;
      throw error;
    }

    const { object_id, to_character_id, transfer_reason, new_binding_type } = req.body;

    if (!object_id || !isValidHexId(object_id)) {
      const error = new Error('object_id is required and must be a valid hex ID.');
      error.statusCode = 400;
      throw error;
    }

    if (!to_character_id || !isValidHexId(to_character_id)) {
      const error = new Error('to_character_id is required and must be a valid hex ID.');
      error.statusCode = 400;
      throw error;
    }

    if (fromCharacterId === to_character_id) {
      const error = new Error('Cannot transfer an object to the same character.');
      error.statusCode = 400;
      throw error;
    }

    await client.query('BEGIN');

    const inventoryResult = await client.query(
      'SELECT inventory_entry_id, slot_trait_hex_id, binding_type FROM character_inventory WHERE character_id = $1 AND object_id = $2 FOR UPDATE',
      [fromCharacterId, object_id]
    );

    if (inventoryResult.rows.length === 0) {
      await client.query('ROLLBACK');
      const error = new Error('Object not found in source character inventory.');
      error.statusCode = 404;
      throw error;
    }

    const currentEntry = inventoryResult.rows[0];

    const targetResult = await client.query(
      'SELECT character_id FROM character_profiles WHERE character_id = $1',
      [to_character_id]
    );

    if (targetResult.rows.length === 0) {
      await client.query('ROLLBACK');
      const error = new Error('Target character not found.');
      error.statusCode = 404;
      throw error;
    }

    const transferId = await generateHexId('object_transfer_id');

    await client.query(
      'INSERT INTO object_transfer_history (transfer_id, object_id, from_character_id, to_character_id, transfer_reason, transferred_by, previous_binding_type, new_binding_type) VALUES ($1, $2, $3, $4, $5, $6, $7, $8)',
      [
        transferId,
        object_id,
        fromCharacterId,
        to_character_id,
        transfer_reason || 'admin_transfer',
        'admin',
        currentEntry.binding_type,
        new_binding_type || currentEntry.binding_type,
      ]
    );

    await client.query(
      'UPDATE character_inventory SET character_id = $1, source_character_id = $2, acquired_at = CURRENT_TIMESTAMP, acquisition_method = $3, binding_type = $4 WHERE inventory_entry_id = $5',
      [
        to_character_id,
        fromCharacterId,
        'transfer',
        new_binding_type || currentEntry.binding_type,
        currentEntry.inventory_entry_id,
      ]
    );

    await client.query('COMMIT');

    const duration = Math.round(performance.now() - startTime);

    logger.info('Object transferred', {
      transferId,
      objectId: object_id,
      fromCharacterId,
      toCharacterId: to_character_id,
      reason: transfer_reason || 'admin_transfer',
      durationMs: duration
    });

    res.json({
      success: true,
      transfer: {
        transfer_id: transferId,
        object_id: object_id,
        from_character_id: fromCharacterId,
        to_character_id: to_character_id,
        transfer_reason: transfer_reason || 'admin_transfer',
      }
    });

  } catch (error) {
    try { await client.query('ROLLBACK'); } catch (rollbackErr) { /* already rolled back */ }
    next(error);
  } finally {
    client.release();
  }
});


