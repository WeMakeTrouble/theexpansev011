/* ============================================================================
 * GET /:id — Single Character Detail
 * ============================================================================
 * Returns complete character data including:
 * - Profile fields (name, category, description, image_url, etc.)
 * - Personality (Big Five + PAD + archetype link + generation)
 * - Archetype template info (if assigned)
 * - All 30 facet scores (if populated)
 * - All attached images with roles
 * - Degradation rules (if category has them)
 *
 * All four data queries run in parallel via Promise.all for minimum
 * latency. Each query is independent and does not depend on the
 * result of another.
 * ============================================================================ */

router.get('/:id', async (req, res, next) => {
  const startTime = performance.now();

  try {
    const characterId = decodeURIComponent(req.params.id);

    if (!isValidHexId(characterId)) {
      const error = new Error('Invalid character ID format. Expected #XXXXXX (6 uppercase hex digits).');
      error.statusCode = 400;
      throw error;
    }

    const [profileResult, personalityResult, facetResult, imagesResult] = await Promise.all([

      pool.query(`
        SELECT
          cp.character_id,
          cp.character_name,
          cp.category,
          cp.description,
          cp.image_url,
          cp.is_active,
          cp.is_b_roll_autonomous,
          cp.trait_generation_enabled,
          cp.forgetting_enabled,
          cp.omiyage_giving_affinity,
          cp.omiyage_receiving_comfort,
          cp.current_location,
          cp.created_at,
          cp.updated_at
        FROM character_profiles cp
        WHERE cp.character_id = $1
      `, [characterId]),

      pool.query(`
        SELECT
          cper.openness,
          cper.conscientiousness,
          cper.extraversion,
          cper.agreeableness,
          cper.neuroticism,
          cper.pad_baseline_p,
          cper.pad_baseline_a,
          cper.pad_baseline_d,
          cper.archetype_id,
          cper.archetype_generation,
          cper.archetype_source_character,
          cper.idiolect_region,
          cper.idiolect_education_level,
          cper.idiolect_age,
          cper.working_memory_capacity,
          cper.trait_activation_stress_threshold,
          oa.archetype_code,
          oa.archetype_name
        FROM character_personality cper
        LEFT JOIN ocean_archetypes oa
          ON cper.archetype_id = oa.archetype_id
        WHERE cper.character_id = $1
      `, [characterId]),

      pool.query(`
        SELECT
          cfs.facet_code,
          cfs.domain,
          cfs.score,
          cfs.source,
          nf.facet_name,
          nf.facet_number
        FROM character_facet_scores cfs
        JOIN neo_facets nf ON cfs.facet_code = nf.facet_code
        WHERE cfs.character_id = $1
        ORDER BY cfs.domain, nf.facet_number
      `, [characterId]),

      pool.query(`
        SELECT
          ema.attachment_id,
          ema.attachment_role,
          ema.sort_order,
          ma.asset_id,
          ma.url,
          ma.description,
          ma.mime_type,
          ma.file_size,
          ma.tags
        FROM entity_media_attachments ema
        JOIN multimedia_assets ma ON ema.asset_id = ma.asset_id
        WHERE ema.entity_type = 'character'
          AND ema.entity_id = $1
        ORDER BY ema.attachment_role, ema.sort_order
      `, [characterId])

    ]);

    if (profileResult.rows.length === 0) {
      const error = new Error('Character not found');
      error.statusCode = 404;
      throw error;
    }

    const profile = profileResult.rows[0];
    const personality = personalityResult.rows[0] || null;

    let degradationRules = null;
    if (profile.category) {
      const degradationResult = await pool.query(`
        SELECT
          category,
          source_character_id,
          degradation_per_generation,
          floor_values,
          ceiling_values,
          facet_variance_decay,
          notes
        FROM character_degradation_rules
        WHERE category = $1
      `, [profile.category]);

      if (degradationResult.rows.length > 0) {
        degradationRules = degradationResult.rows[0];
      }
    }

    const duration = Math.round(performance.now() - startTime);

    logger.info('Character detail fetched', {
      characterId,
      characterName: profile.character_name,
      category: profile.category,
      hasPersonality: personality !== null,
      facetCount: facetResult.rows.length,
      imageCount: imagesResult.rows.length,
      hasDegradationRules: degradationRules !== null,
      durationMs: duration
    });

    res.json({
      success: true,
      character: {
        profile,
        personality,
        facets: facetResult.rows,
        images: imagesResult.rows,
        degradationRules
      }
    });

  } catch (error) {
    next(error);
  }
});

