import { Router } from 'express';
import pool from '../db/pool.js';
import { createModuleLogger } from '../utils/logger.js';
import generateHexId from '../utils/hexIdGenerator.js';

const logger = createModuleLogger('AdminCharacters');
const router = Router();

/**
 * Maximum characters returned by list endpoint.
 * Defensive cap even though current count is ~50.
 * @type {number}
 */
const LIST_LIMIT = 200;

/**
 * Validates a hex ID matches the canonical #XXXXXX format.
 * Used on all route params before any database query executes.
 *
 * @param {string} id - The ID to validate
 * @returns {boolean} True if valid hex ID format
 */
function isValidHexId(id) {
  return /^#[0-9A-F]{6}$/.test(id);
}

/* ============================================================================
 * GET / — List All Characters
 * ============================================================================
 * Returns all characters with their profile image (primary attachment),
 * personality summary, and archetype name. Capped at LIST_LIMIT rows.
 * ============================================================================ */

router.get('/', async (req, res, next) => {
  const startTime = performance.now();

  try {
    const result = await pool.query(`
      SELECT
        cp.character_id,
        cp.character_name,
        cp.category,
        cp.description,
        cp.is_active,
        cp.is_b_roll_autonomous,
        cper.openness,
        cper.conscientiousness,
        cper.extraversion,
        cper.agreeableness,
        cper.neuroticism,
        cper.archetype_id,
        oa.archetype_code,
        oa.archetype_name,
        ma.url AS profile_image_url,
        ma.asset_id AS profile_image_asset_id
      FROM character_profiles cp
      LEFT JOIN character_personality cper
        ON cp.character_id = cper.character_id
      LEFT JOIN ocean_archetypes oa
        ON cper.archetype_id = oa.archetype_id
      LEFT JOIN entity_media_attachments ema
        ON ema.entity_type = 'character'
        AND ema.entity_id = cp.character_id
        AND ema.attachment_role = 'primary'
      LEFT JOIN multimedia_assets ma
        ON ema.asset_id = ma.asset_id
      ORDER BY cp.character_name ASC
      LIMIT $1
    `, [LIST_LIMIT]);

    const duration = Math.round(performance.now() - startTime);

    logger.info('Character list fetched', {
      count: result.rows.length,
      durationMs: duration
    });

    res.json({
      success: true,
      characters: result.rows,
      count: result.rows.length
    });

  } catch (error) {
    next(error);
  }
});

